    <script>
        // Exponential value
        const base = 2;
        const exponent = 3;
        const resultExponential = Math.pow(base, exponent);
        console.log(`Exponential Value: ${resultExponential}`);

        // Limit the number of digits after the decimal point
        const numberToRound = 3.14159265359;
        const roundedNumber = numberToRound.toFixed(2); // Limit to 2 decimal places
        console.log(`Rounded Number: ${roundedNumber}`);

        // Convert a number to a string
        const num = 42;
        const numAsString = num.toString();
        console.log(`Number as String: ${numAsString}`);

        // Return negative infinity using the Number object
        const negativeInfinity = Number.NEGATIVE_INFINITY;
        console.log(`Negative Infinity: ${negativeInfinity}`);
    </script>
